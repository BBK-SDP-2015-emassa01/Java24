class Grade {
public static void main(String[] arguments){
 char grade = 'F';
 
 switch(grade) {
     case 'A':
         System.out.println("you got an a well done");
         break;
     case 'B':
         System.out.println("you got an b well done");
         break;
     case 'C':
         System.out.println("you got an c well done");
         break;
     default:
         System.out.println("you are still a star!");
 
  }

 }

}